# Conceitos de Orientação a Objetos (OO)
# - Programação orientada a objetos (POO) é um paradigma de programação baseado no conceito de "objetos",
# que podem conter dados na forma de campos, também conhecidos como atributos, e códigos, na forma de metodos.

# Classes / Getter / Setter
# - Uma classe é uma estrutura que abstrai um conjunto de objetos com características similares.
# Uma classe define o comportamento de seus objetos através de métodos e seus estados através de atributos.
# - Getter e Setter são métodos que permitem obter e modificar os valores de atributos de uma classe.

# Objetos
# - Um objeto é uma instância de uma classe.
# - Um objeto é uma variável que armazena uma referência a um valor que é instância de uma classe.

# Atributos
# - Atributos são características de um objeto.
# - Atributos são variáveis que armazenam valores que são instâncias de uma classe.

# Métodos (funções)
# - Métodos são funções que definem o comportamento de um objeto.

# Herança
# - Herança é um recurso que permite que classes compartilhem atributos e métodos,

# Polimorfismo
# - Polimorfismo é o princípio pelo qual duas ou mais classes derivadas de uma mesma superclasse podem invocar métodos que têm a mesma assinatura mas comportamentos distintos.


